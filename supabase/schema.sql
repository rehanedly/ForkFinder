-- Create Policies to ensure frontend operates seamlessly for anonymous users (since Auth is not yet built)
CREATE POLICY "Allow public read access on restaurants" ON public.restaurants FOR SELECT USING (true);
CREATE POLICY "Allow public read access on normalized_items" ON public.normalized_items FOR SELECT USING (true);
CREATE POLICY "Allow public read access on menu_items" ON public.menu_items FOR SELECT USING (true);
CREATE POLICY "Allow public read access on reviews" ON public.reviews FOR SELECT USING (true);

-- Allow inserting orders
CREATE POLICY "Allow public insert to orders" ON public.orders FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public read to orders" ON public.orders FOR SELECT USING (true);
CREATE POLICY "Allow public update to orders" ON public.orders FOR UPDATE USING (true);
CREATE POLICY "Allow public insert to order_items" ON public.order_items FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public read to order_items" ON public.order_items FOR SELECT USING (true);
